
public class ArithmeticOperations {

    public static void main(String[] args) {
        // 変数の宣言・初期化
        int num1 = 100; // int型変数num1を宣言し、100を代入
        int num2 = 10; // int型変数num2を宣言し、10を代入
        
        int result = num1 + num2;
        System.out.println("num1 + num2 = " + result);
        
        result = num1 - num2;
        System.out.println("num1 - num2 = " + result);
        
        result = num1 * num2;
        System.out.println("num1 * num2 = " + result);
        
        result = num1 / num2;
        System.out.println("num1 / num2 = " + result);
        
        result = num1 % num2;
        System.out.println("num1 % num2 = " + result);
    }

}
